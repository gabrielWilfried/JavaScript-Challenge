/**
 * Loops  are fundamental construct in Javascript that
 * allows you to execute a block of code repeatedly.
 * In Javascript they are mainly two types of loops:
 * For and While loops
 * 
 * 
 * Task: Printing numbers from 1 to 10 using both for and while loops
 */


// Printing from 1 to 10 using both For and While loops

console.log("printing from 1 to 10 using the For loop");
// using For loop
for(let i = 1; i <= 10; i++){
    console.log(i);
}

console.log("printing from 1 to 10 using the while loop");
//using While loop
let i = 1;
while(i <= 10){
    console.log(i);
    i++;
}


