/**
 * Creating the object person
 * Name : String
 * Age : integer 
 * Address : String 
 */

const person = {
    Name : "gabriel",
    Age : 23,
    Address : "Post school yaounde"
}

/**
 * Accessing and displaying the name of the person
 */

console.log(person.Name);

/**
 * Accessing and displaying the age of the person
 */

console.log(person.Age);

/**
 * Accessing and displaying the address of the person
 */

console.log(person.Address);